'use strict'
/**
 * Build invocation view files with UTF-8 Chinese (ASCII-only source).
 * Usage: node scripts/build-invocation-views.js
 */
const fs = require('fs')
const path = require('path')
const { t } = require('./lib/zh-openplatform-hex')

const ROOT = path.join(__dirname, '..')
const M = {
  noCopy: t('noCopyContent'),
  noDownload: t('noDownloadContent'),
  downloadStarted: t('downloadStarted'),
  loadDetailFail: t('loadDetailFail'),
  downloadRequestFail: t('downloadRequestFail'),
  downloadResponseFail: t('downloadResponseFail'),
  loadRequestFail: t('loadRequestFail'),
  requestJsonCopied: t('requestJsonCopied'),
  copyFailed: t('copyFailed'),
  copied: t('copied'),
  copyFailManual: t('copyFailManual')
}

function write (rel, lines) {
  const full = path.join(ROOT, rel)
  fs.writeFileSync(full, lines.join('\n'), { encoding: 'utf8' })
  console.log('[build] ' + rel)
}

function bodyActions (kind) {
  const isReq = kind === 'request'
  const has = isReq ? 'hasRequestBody' : 'hasResponseBody'
  const size = isReq ? 'requestBodyByteSize' : 'responseBodyByteSize'
  const open = isReq ? 'openRequestBody' : 'openResponseBody'
  const dl = isReq ? 'downloadRequestBody' : 'downloadResponseBody'
  const large = isReq ? t('requestBodyLarge') : t('responseBodyLarge')
  const none = isReq ? t('noRequestBody') : t('noResponseBody')
  return [
    '                <div v-if="detail.' + has + '" class="response-body-actions">',
    '                  <p class="helper">',
    '                    ' + large,
    '                    <span v-if="detail.' + size + '">' + t('persistedAbout') + ' {{ formatBytes(detail.' + size + ') }}' + t('fullStop') + '</span>',
    '                  </p>',
    '                  <div class="action-row">',
    '                    <a-button size="small" type="primary" @click="' + open + '">' + t('modalView') + '</a-button>',
    '                    <a-button size="small" @click="' + dl + '">' + t('downloadTxt') + '</a-button>',
    '                  </div>',
    '                </div>',
    '                <p v-else class="helper">' + none + '</p>'
  ]
}

function buildRequestBodyModal () {
  write('src/views/openPlatform/components/InvocationRequestBodyModal.vue', [
    '<template>',
    '  <a-modal :visible="visible" :title="modalTitle" :width="960" :footer="null" :destroy-on-close="true" @cancel="handleClose">',
    '    <a-spin :spinning="loading">',
    '      <div v-if="payload" class="request-body-modal">',
    '        <div class="meta-row">',
    '          <span>' + t('sizeLabel') + '{{ formatBytes(payload.byteSize) }}</span>',
    '          <a-tag v-if="payload.stored" color="blue">' + t('fullRequest') + '</a-tag>',
    '          <a-tag v-else color="orange">' + t('summaryRebuild') + '</a-tag>',
    '        </div>',
    '        <div class="toolbar">',
    '          <a-button size="small" @click="copyBody">' + t('copyJson') + '</a-button>',
    '          <a-button size="small" @click="downloadBody">' + t('downloadTxt') + '</a-button>',
    '        </div>',
    '        <pre class="code-block">{{ payload.bodyFormatted }}</pre>',
    '      </div>',
    '      <a-empty v-else-if="!loading" description="' + t('noRequestBody') + '" />',
    '    </a-spin>',
    '  </a-modal>',
    '</template>',
    '',
    '<script>',
    'import { getInvocationRequestBody } from \'@/api/openPlatform/invocation\'',
    '',
    'export default {',
    '  name: \'InvocationRequestBodyModal\',',
    '  props: {',
    '    visible: { type: Boolean, default: false },',
    '    invocationId: { type: String, default: \'\' }',
    '  },',
    '  data () { return { loading: false, payload: null } },',
    '  computed: {',
    '    modalTitle () {',
    '      return this.invocationId ? (\'Request Body - \' + this.invocationId) : \'Request Body\'',
    '    }',
    '  },',
    '  watch: {',
    '    visible (val) { if (val) this.loadBody(); else this.payload = null },',
    '    invocationId () { if (this.visible) this.loadBody() }',
    '  },',
    '  methods: {',
    '    loadBody () {',
    '      if (!this.invocationId) { this.payload = null; return }',
    '      this.loading = true',
    '      getInvocationRequestBody(this.invocationId).then(data => { this.payload = data || null }).catch(err => {',
    '        this.payload = null',
    '        this.$message.error((err && err.message) || \'' + M.loadRequestFail.replace(/'/g, '\\\'') + '\')',
    '      }).finally(() => { this.loading = false })',
    '    },',
    '    handleClose () { this.$emit(\'close\') },',
    '    copyBody () {',
    '      const text = this.payload && this.payload.bodyFormatted',
    '      if (!text) { this.$message.warning(\'' + M.noCopy.replace(/'/g, '\\\'') + '\'); return }',
    '      if (navigator.clipboard && navigator.clipboard.writeText) {',
    '        navigator.clipboard.writeText(text).then(() => {',
    '          this.$message.success(\'' + M.requestJsonCopied.replace(/'/g, '\\\'') + '\')',
    '        }).catch(() => this.fallbackCopy(text))',
    '      } else this.fallbackCopy(text)',
    '    },',
    '    fallbackCopy (text) {',
    '      const ta = document.createElement(\'textarea\')',
    '      ta.value = text',
    '      ta.style.position = \'fixed\'',
    '      ta.style.left = \'-9999px\'',
    '      document.body.appendChild(ta)',
    '      ta.select()',
    '      try {',
    '        document.execCommand(\'copy\')',
    '        this.$message.success(\'' + M.requestJsonCopied.replace(/'/g, '\\\'') + '\')',
    '      } catch (e) { this.$message.error(\'' + M.copyFailed.replace(/'/g, '\\\'') + '\') }',
    '      document.body.removeChild(ta)',
    '    },',
    '    downloadBody () {',
    '      const text = this.payload && this.payload.bodyFormatted',
    '      if (!text) { this.$message.warning(\'' + M.noDownload.replace(/'/g, '\\\'') + '\'); return }',
    '      const filename = (this.invocationId || \'request\') + \'-request.txt\'',
    '      const blob = new Blob([text], { type: \'text/plain;charset=utf-8\' })',
    '      const url = URL.createObjectURL(blob)',
    '      const link = document.createElement(\'a\')',
    '      link.href = url',
    '      link.download = filename',
    '      document.body.appendChild(link)',
    '      link.click()',
    '      document.body.removeChild(link)',
    '      URL.revokeObjectURL(url)',
    '      this.$message.success(\'' + M.downloadStarted.replace(/'/g, '\\\'') + '\')',
    '    },',
    '    formatBytes (size) {',
    '      if (size === undefined || size === null || size === \'\') return \'-\'',
    '      const num = Number(size)',
    '      if (Number.isNaN(num) || num <= 0) return \'-\'',
    '      if (num < 1024) return num + \' B\'',
    '      if (num < 1024 * 1024) return (num / 1024).toFixed(1) + \' KB\'',
    '      return (num / 1024 / 1024).toFixed(2) + \' MB\'',
    '    }',
    '  }',
    '}',
    '</script>',
    '',
    '<style scoped>',
    '.request-body-modal { min-height: 120px; }',
    '.meta-row { display: flex; align-items: center; gap: 12px; margin-bottom: 8px; color: rgba(0,0,0,.45); font-size: 12px; }',
    '.toolbar { display: flex; gap: 8px; margin-bottom: 8px; }',
    '.code-block { background: #fafafa; border: 1px solid #f0f0f0; padding: 12px; border-radius: 2px; font-size: 12px; overflow: auto; max-height: 60vh; white-space: pre-wrap; word-break: break-all; margin: 0; }',
    '</style>'
  ])
}

